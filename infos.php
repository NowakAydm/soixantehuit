            Huit décennies plus tard, dans un bâtiment résolument contemporain, entièrement dédié au 7e art, La Cinémathèque française dévoile plus que jamais le cinéma de manière unique grâce à <b>une des plus belle collections au monde sur le cinéma</b> et à ses nombreuses activités :<br>
            Des <b>expositions autour de personnalités majeures ou de grands moments de l’histoire du cinéma</b> (Tim Burton, Metropolis, Lanternes magiques, Pedro Almodovar, François Truffaut, Jacques Tati, Stanley Kubrick, Scorsese,...), des <b>films et conférences</b> (40 films par semaine, 4 salles de cinéma), une <b>Bibliothèque du Film</b> (22 500 ouvrages, 500 revues spécialisées, 23 000 affiches, 12 000 films...), des <b>collections</b> (40 000 films, 3 000 costumes, 23 000 affiches, 14 500 dessins, 2 300 objets…), un <b>Musée du cinéma</b>, des <b>activités pour le jeune public</b> et une <b>Librairie</b>.
